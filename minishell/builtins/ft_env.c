#include "../includes/minishell.h"

void	ft_env(t_env *env)
{
	while (env)
	{
		if (env->value && ft_strchr(env->value, '='))
			ft_putendl_fd(env->value, STDOUT_FILENO);
		env = env->next;
	}
}
